package puppeteer;

public class FileSet
{
	String spriteWanted;
	String spriteAvailable;
	String attWanted;
	String attAvailable;
	public FileSet(String spriteWanted, String spriteAvailable, String attWanted, String attAvailable) {
		this.spriteWanted = spriteWanted;
		this.spriteAvailable = spriteAvailable;
		this.attWanted = attWanted;
		this.attAvailable = attAvailable;
	}
}
